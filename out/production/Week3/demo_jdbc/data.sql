CREATE TABLE bus_route
(
    BUS_ROUTE_ID NUMBER(3,0) NOT NULL ENABLE,
	BUS_ROUTE_RANGE NUMBER(10,0),
	BUS_ROUTE_STOP_NUMBER NUMBER(3,0),
    CONSTRAINT "BUS_ROUTE_PK" PRIMARY KEY ("BUS_ROUTE_ID")
);
CREATE TABLE driver
(
    DRIVER_ID NUMBER(6,0) NOT NULL ENABLE,
	DRIVER_NAME NVARCHAR2(50),
	DRIVER_ADDRESS NVARCHAR2(50),
	DRIVER_PHONE NVARCHAR2(15),
	DRIVER_LEVEL NVARCHAR2(1),
	 CONSTRAINT "DRIVER_PK" PRIMARY KEY ("DRIVER_ID")
);
CREATE TABLE driving
(
    DRIVER_ID NUMBER(5,0),
    BUS_ROUTE_ID NUMBER(3,0),
	ROUTEQUANTITY NUMBER(4,0),
    CONSTRAINT "FK_DRIVER" FOREIGN KEY ("DRIVER_ID")
    REFERENCES "DRIVER" ("DRIVER_ID") ENABLE,
    CONSTRAINT "FK_BUS_ROUTE" FOREIGN KEY ("BUS_ROUTE_ID")
    REFERENCES "BUS_ROUTE" ("BUS_ROUTE_ID") ENABLE
)

